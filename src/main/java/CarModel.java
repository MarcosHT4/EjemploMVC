public class CarModel {

    private String brand;
    private String model;
    private String color;
    private int numberOfSeats;
    private int numberOfMirrors;
    private boolean hasAirConditioner;
    private String engine;
    private boolean hasShockAbsorber;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getNumberOfSeats() {
        return numberOfSeats;
    }

    public void setNumberOfSeats(int numberOfSeats) {
        this.numberOfSeats = numberOfSeats;
    }

    public int getNumberOfMirrors() {
        return numberOfMirrors;
    }

    public void setNumberOfMirrors(int numberOfMirrors) {
        this.numberOfMirrors = numberOfMirrors;
    }

    public boolean isHasAirConditioner() {
        return hasAirConditioner;
    }

    public void setHasAirConditioner(boolean hasAirConditioner) {
        this.hasAirConditioner = hasAirConditioner;
    }

    public String getEngine() {
        return engine;
    }

    public void setEngine(String engine) {
        this.engine = engine;
    }

    public boolean isHasShockAbsorber() {
        return hasShockAbsorber;
    }

    public void setHasShockAbsorver(boolean hasShockAbsorber) {
        this.hasShockAbsorber = hasShockAbsorber;
    }
}
